
// 1) Printing Array elements with Comma delimiters
//Description
//Input: Array of elements
// ["h","o","l","a"]
// Output: String with comma delimited elements of the array in th same order.
// "h,o,l,a"

function printArray(array){
    //show me the code!
    return array.join(",");
}

const arr=['h','o','l','a'];
console.log(printArray(arr));